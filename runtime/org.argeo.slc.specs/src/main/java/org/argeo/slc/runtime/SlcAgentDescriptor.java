package org.argeo.slc.runtime;

public class SlcAgentDescriptor {
	private String uuid;
	private String host;

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

}
